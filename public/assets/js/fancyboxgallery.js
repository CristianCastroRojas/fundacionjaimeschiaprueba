$(document).ready(function () {
    // Inicializa Fancybox
    $(".fancybox").fancybox({
        // Opciones de configuración
        loop: true, // Permite la navegación continua a través de las imágenes
        zoom: true, // Habilita el zoom en las imágenes
        zoomMax: 3, // Establece el nivel máximo de zoom (opcional)
    });
});